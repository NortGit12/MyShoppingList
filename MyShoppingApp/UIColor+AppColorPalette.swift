import UIKit

extension UIColor {
    
    //==================================================
    // MARK: - Methods
    //==================================================
    
    static func basicBlueColor() -> UIColor {
        return UIColor(red: 0.094, green: 0.537, blue: 0.710, alpha: 1.00)
    }
    
    static func basicGrayColor() -> UIColor {
        return UIColor(red: 0.592, green: 0.592, blue: 0.592, alpha: 1.00)
    }
}
